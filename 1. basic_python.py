# Loop,Conditional Statements,Function

# Loop
# for loop
for i in range(5):
    print(i)
    
# while loop
i = 0
while i < 5:
    print(i)
    i += 1
    
# Conditional Statements
# if
if True:
    print("True")

# if else
if True:
    print("True")
else:
    print("False")

# if elif else
if True:
    print("True")
elif False:
    print("False")
else:
    print("None")
    
# Function
def func():
    print("Hello")